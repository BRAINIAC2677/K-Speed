const timerBox = document.querySelector(".timer-box");
const scoreBox = document.querySelector(".score-box");
const speedBox = document.querySelector(".speed-box");
const testText = document.querySelector(".test-text");
const inputText = document.querySelector("#input-text");
const startBtn = document.querySelector("button");

let time = 60;
let testString = "";
let curCharIndex = 0;
let score = 0;
let wordCount = 0;
let wordPerTurn = 15;

function stopWatch() {
  if (time == 0) {
    //test finish
    inputText.disabled = true;
    time = 60;
    timerBox.innerText = time;
    testText.innerText = "";
  } else {
    setTimeout(() => {
      time -= 1;
      timerBox.innerText = time;
      stopWatch();
    }, 1000);
  }
}

function createHtml() {
  return `${testString.slice(0, curCharIndex)}<span class="highlight">${
    testString[curCharIndex]
  }</span>${testString.slice(curCharIndex + 1)}`;
}

function getRandom(min, max) {
  return Math.floor(Math.random() * (max - min)) + min;
}

function generateTestString(length) {
  let temp = [];
  for (let i = 0; i < length; i++) {
    let index = getRandom(0, words.length);
    temp.push(words[index]);
  }
  testString = temp.join(" ");
  curCharIndex = 0;
}

function started() {
  inputText.focus();
  if (time != 60) {
    return;
  }
  inputText.disabled = false;
  score = 0;
  wordCount = 0;
  speedBox.innerText = `WPM: ${0}`;
  scoreBox.innerText = `Score: ${score}`;
  generateTestString(wordPerTurn);
  testText.innerHTML = createHtml();
  inputText.focus();
  stopWatch();
}

function keyTyped(event) {
  let typed = event.target.value;
  event.target.value = "";
  if (typed === testString[curCharIndex]) {
    score += 1;
    curCharIndex += 1;
    scoreBox.innerText = `Score: ${score}`;
    if (curCharIndex == testString.length) {
      generateTestString(wordPerTurn);
    }
    if (testString[curCharIndex] == " ") {
      wordCount += 1;
    }
    if (time != 60) {
      speedBox.innerText = `WPM: ${Math.floor((wordCount / (60 - time)) * 60)}`;
    }
    testText.innerHTML = createHtml();
  } else {
    score -= 3;
  }
}

startBtn.addEventListener("click", started);
inputText.addEventListener("input", keyTyped);
